# to check grades
g=int(input("enter grade"))
# The marks must be between the range of 0-100
if (g <= 20):
        print("F")
elif (g >= 21) and (g <= 39):
        print("E")
elif (g >= 40) and (g <= 49):
        print("D")
elif (g >= 50) and (g <= 64):
        print("C")
elif (g >= 65) and (g <= 74):
        print("B")
elif (g >= 75) and (g <= 100):
        print("A")
else:
        print("invalid value")


